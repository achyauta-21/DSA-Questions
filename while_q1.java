
// print number from 1 to n
import java.util.*;
public class while_q1 {
public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    System.out.println("Enter the value of n");
    int n = sc.nextInt();
    int counter =0;
    while(counter<n){
        System.out.println(counter);
        counter++;
    }
}

    
}
